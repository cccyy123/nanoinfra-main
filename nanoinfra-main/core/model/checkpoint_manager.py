def should_save_checkpoint(step: int, max_steps: int, save_every: int) -> bool:
    """
    Determine if checkpoint should be saved at current step.

    Args:
        step: Current training step
        max_steps: Total training steps
        save_every: Save frequency (-1 means save only at end)

    Returns:
        bool: True if checkpoint should be saved
    """
    # Always save at the last step
    if step == max_steps - 1:
        return True

    # -1 means save only at the end
    if save_every == -1:
        return False

    # Save at regular intervals
    return step % save_every == 0


def cleanup_old_checkpoints(save_dir: str, keep_last_n: int, current_step: int):
    """
    Remove old checkpoint directories, keeping only the last N.

    Only considers checkpoints with step <= current_step for cleanup.
    Checkpoints with step > current_step (from previous runs) are ignored
    and a warning is printed.

    Args:
        save_dir: Base directory containing checkpoint directories
        keep_last_n: Number of checkpoints to keep (-1 means keep all)
        current_step: Current training step (only cleanup checkpoints <= this step)
    """
    if keep_last_n == -1:
        return

    # Find all step_* directories
    checkpoint_dirs = glob.glob(os.path.join(save_dir, "step_*"))

    # Extract step numbers and categorize
    valid_checkpoints = []  # step <= current_step
    future_checkpoints = []  # step > current_step (from old runs)

    for path in checkpoint_dirs:
        dirname = os.path.basename(path)
        match = re.fullmatch(r"step_(\d+)", dirname)
        if match:
            step = int(match.group(1))
            if step <= current_step:
                valid_checkpoints.append((step, path))
            else:
                future_checkpoints.append((step, path))

    # Warn about future checkpoints (from previous runs)
    from core.utils import print0
    if future_checkpoints:
        future_steps = sorted([step for step, _ in future_checkpoints], reverse=True)
        print0(f"  ⚠️  Found {len(future_checkpoints)} checkpoint(s) with larger steps {future_steps} (keep_last_n won't delete these)")

    # Check if we need to cleanup valid checkpoints
    if len(valid_checkpoints) <= keep_last_n:
        return

    # Sort by step number (newest first)
    valid_checkpoints.sort(key=lambda x: x[0], reverse=True)

    # Remove old checkpoints (only those <= current_step)
    for _, path in valid_checkpoints[keep_last_n:]:
        print0(f"  Removing old checkpoint: {path}")
        shutil.rmtree(path)


def load_checkpoint_if_needed(
    checkpoint_config: Dict[str, Any],
    model,
    optimizers: List,
    dataloader,
    rank: int,
    world_size: int,
) -> tuple[int, Optional[Dict]]:
    """
    Load checkpoint if needed based on configuration.

    Handles checkpoint loading logic with priority:
    1. resume_from (full training resume: model + optimizer + dataloader)
    2. init_model_from (model weights only, fresh training)
    3. No loading (random initialization)

    This is the main interface for checkpoint loading in core training.
    Matches the pattern of save_checkpoint_if_needed for consistency.

    Args:
        checkpoint_config: Checkpoint configuration dict (from config['checkpoint'])
        model: FSDP-wrapped model
        optimizers: List of optimizers
        dataloader: Dataloader (must have set_state() method for resume support)
        rank: Current rank
        world_size: Total number of ranks

    Returns:
        (start_step, trainer_state):
            - start_step: Step to start/resume training from (0 for fresh, N+1 for resume)
            - trainer_state: Trainer internal state from checkpoint (None for fresh training)

    Examples:
        # In Trainer.__init__():
        self.start_step, self._resumed_trainer_state = load_checkpoint_if_needed(
            checkpoint_config=self.checkpoint_config,
            model=self.model,
            optimizers=self.optimizers,
            dataloader=self.dataloader,
            rank=self.rank,
            world_size=self.world_size,
        )
    """
    from core.utils import print0

    resume_from = checkpoint_config.get('resume_from')
    init_model_from = checkpoint_config.get('init_model_from')
    save_dir = checkpoint_config.get('save_dir')

    # Priority 1: Full checkpoint resume (model + optimizer + dataloader)
    if resume_from:
        resolved_path = resolve_checkpoint_path(resume_from, save_dir=save_dir, rank=rank)
        if resolved_path:
            # Load full checkpoint
            if rank == 0:
                print0(f"\nResuming from checkpoint: {resolved_path}")

            metadata = load_checkpoint_dcp(
                checkpoint_dir=resolved_path,
                model=model,
                load_optimizer=True,
                optimizers=optimizers,
                rank=rank,
                world_size=world_size,
            )

            # Resume from saved step (checkpoint at step N → resume at N+1)
            saved_step = metadata['step']
            start_step = saved_step + 1

            # Resume dataloader state if available and supported
            dataloader_state = metadata.get('dataloader_state')
            if dataloader_state and hasattr(dataloader, 'set_state'):
                dataloader.set_state(dataloader_state)
                if rank == 0:
                    sampler = getattr(dataloader, 'sampler', None)
                    print0(f"  ✓ Resumed dataloader state: {sampler}" if sampler else "  ✓ Resumed dataloader state")

            # Extract trainer state for resuming
            trainer_state = metadata.get('trainer_state')

            if rank == 0:
                print0(f"  ✓ Resumed from step {saved_step} checkpoint (will continue from step {start_step})")
                print0("")

            return start_step, trainer_state

        elif init_model_from:
            # resume_from was set but no checkpoint found, fall back to init_model_from
            if rank == 0:
                print0(f"\n⚠️  No checkpoint found for resume_from, falling back to init_model_from")

    # Priority 2: Model weight initialization only (fresh training)
    if init_model_from:
        if isinstance(init_model_from, str) and init_model_from.lower() == "auto":
            raise ValueError(
                "checkpoint.init_model_from does not support 'auto'. "
                "If you want to resume training, use checkpoint.resume_from=auto instead."
            )

        ckpt_path = resolve_checkpoint_path(init_model_from, save_dir=None, rank=rank)
        if ckpt_path is None:
            raise FileNotFoundError(f"Init checkpoint not found: {init_model_from}")

        if rank == 0:
            print0(f"\nInitializing model from checkpoint: {ckpt_path}")
            print0(f"  (Model weights only - optimizer and dataloader start fresh)")

        load_model_only(
            checkpoint_dir=ckpt_path,
            model=model,
            rank=rank,
            world_size=world_size,
        )

        if rank == 0:
            print0(f"  ✓ Model initialized from pretrained weights")
            print0(f"  ✓ Training will start from step 0\n")

        return 0, None

    # Priority 3: No loading (random initialization)
    return 0, None


def save_checkpoint_if_needed(
    step: int,
    model,
    optimizers: List,
    config: Dict[str, Any],
    rank: int,
    world_size: int,
    dataloader_state: Optional[Dict] = None,
    trainer_state: Optional[Dict] = None,
    dataloader=None,
):
    """
    Save checkpoint if needed based on configuration and current step.

    Handles checkpoint saving logic including:
    - Checking if checkpoint should be saved
    - Saving checkpoint using DCP
    - Cleaning up old checkpoints

    This is the main interface for checkpoint saving in core training.
    Stage managers can use this function or implement their own logic.

    Uses config from outside checkpoint section:
        - max_steps: Total training steps

    Args:
        step: Current training step
        model: FSDP-wrapped model
        optimizers: List of optimizers
        config: Full training configuration dict (reads config['checkpoint'])
        rank: Current rank
        world_size: Total number of ranks
        dataloader_state: Optional dataloader state for resuming
        trainer_state: Optional trainer internal state (EMA loss, timing, etc.)
    """
    # Extract checkpoint config and max_steps
    checkpoint_config = config.get('checkpoint', {})
    max_steps = config['max_steps']

    # Extract checkpoint settings
    save_dir = os.path.expandvars(checkpoint_config['save_dir'])
    save_every = checkpoint_config['save_every']
    keep_last_n = checkpoint_config['keep_last_n']

    # Check if we should save
    if not should_save_checkpoint(step, max_steps, save_every):
        return

    # Save checkpoint
    ckpt_dir = os.path.join(save_dir, f"step_{step:06d}")

    # Delete existing checkpoint directory to avoid overwrite warnings
    # Only rank 0 performs the deletion
    if rank == 0 and os.path.exists(ckpt_dir):
        shutil.rmtree(ckpt_dir)

    # Wait for rank 0 to finish deletion before all ranks proceed
    if world_size > 1:
        torch.distributed.barrier()

    meta_data = {'step': step}
    # (model_config recording lives in save_checkpoint_dcp — the ONE writer —
    #  as the full asdict of model.config, so every save path gets the complete
    #  architecture. A partial 4-field copy used to live here; it shadowed the
    #  full record and a from-meta rebuild then filled geometry from defaults.)

    # Add trainer internal state if provided
    if trainer_state:
        meta_data['trainer_state'] = trainer_state

    save_checkpoint_dcp(
        checkpoint_dir=ckpt_dir,
        model=model,
        optimizers=optimizers,
        meta_data=meta_data,
        rank=rank,
        world_size=world_size,
        dataloader_state=dataloader_state,
    )

    # Print and cleanup (only on rank 0)
    if rank == 0:
        from core.utils import print0
        loader_info = f" | {dataloader}" if dataloader is not None else ""
        print0(f"✓ Checkpoint saved: {ckpt_dir}{loader_info}")
        cleanup_old_checkpoints(save_dir, keep_last_n, current_step=step)
