"""
Checkpoint management for FSDP models.

Provides:
- Low-level DCP functions (save_checkpoint_dcp, load_checkpoint_dcp, find_latest_checkpoint)
- High-level checkpoint saving (save_checkpoint_if_needed) for core training
"""

import os
import json
import glob
import re
import shutil
from dataclasses import asdict, is_dataclass
import torch
import torch.distributed.checkpoint as dcp
from torch.distributed.checkpoint import FileSystemWriter, FileSystemReader
from torch.distributed.checkpoint.state_dict import get_state_dict, set_state_dict
from typing import List, Optional, Dict, Any


def save_checkpoint_dcp(
    checkpoint_dir: str,
    model,
    optimizers: List,
    meta_data: Dict[str, Any],
    rank: int = 0,
    world_size: int = 1,
    dataloader_state: Optional[Dict] = None,
) -> None:
    """
    Save FSDP checkpoint using DCP.

    Args:
        checkpoint_dir: Directory to save checkpoint (created if not exists)
        model: FSDP model (contains DTensor parameters)
        optimizers: List of optimizers
        meta_data: Dictionary of metadata to save (must include 'step')
        rank: Current rank
        world_size: Total number of ranks
        dataloader_state: Optional dataloader state for resuming

    Saves:
        - checkpoint_dir/.metadata (JSON, generated by DCP)
        - checkpoint_dir/__0_0.distcp, __1_0.distcp, ... (sharded)
        - checkpoint_dir/meta.json (custom metadata + dataloader_state, rank 0 only)
    """
    # Create checkpoint directory
    os.makedirs(checkpoint_dir, exist_ok=True)

    # Use get_state_dict() to properly handle FSDP model and optimizer states
    # Returns tuple: (model_state_dict, optim_state_dict)
    model_state_dict, optim_state_dict = get_state_dict(model, optimizers)

    # Build state_dict for DCP
    state_dict = {
        "model": model_state_dict,
        "optimizer": optim_state_dict,
    }

    # DCP save (all ranks participate, each writes its own shard)
    # overwrite=True: Handle NFS cache inconsistency in multi-node environments
    dcp.save(
        state_dict=state_dict,
        storage_writer=FileSystemWriter(checkpoint_dir, overwrite=True),
    )

    # Save custom metadata (rank 0 only)
    if rank == 0:
        # Merge dataloader_state into metadata if provided
        if dataloader_state is not None:
            meta_data = {**meta_data, 'dataloader_state': dataloader_state}

        # Self-description: record the architecture this checkpoint was trained
        # with (model.config — LMSystem exposes the trunk's GPTConfig). The live
        # model is the ONLY authority for this key, so it is written
        # UNCONDITIONALLY — a caller-supplied 'model_config' is by definition
        # less authoritative than the model being saved. Loaders recover it via
        # gpt_config_from_meta(); _validate_model_config audits every load
        # against it. A config-less model produces an old-format checkpoint
        # (validation skips: you cannot record what does not exist).
        cfg = getattr(model, 'config', None)
        if is_dataclass(cfg):
            meta_data = {**meta_data, 'model_config': asdict(cfg)}
        # Architecture tag: an ASSEMBLY fact recorded by build_system onto the
        # System (type(trunk) at save time would lie — fully_shard rewrites the
        # instance's class to FSDP{ClassName}). Loaders audit the caller's
        # trunk_cls choice against it (load_system's checked default).
        arch = getattr(model, 'arch', None)
        if arch is not None:
            meta_data = {**meta_data, 'model_arch': arch}

        meta_path = os.path.join(checkpoint_dir, "meta.json")
        with open(meta_path, "w") as f:
            json.dump(meta_data, f, indent=2)


def load_checkpoint_dcp(
    checkpoint_dir: str,
    model,
    load_optimizer: bool = False,
    optimizers: Optional[List] = None,
    rank: int = 0,
    world_size: int = 1,
) -> Dict[str, Any]:
    """
    Load FSDP checkpoint using DCP.

    Args:
        checkpoint_dir: Directory containing checkpoint
        model: FSDP model (will be filled with loaded weights)
        load_optimizer: Whether to load optimizer state
        optimizers: List of optimizers to load into (if load_optimizer=True)
        rank: Current rank
        world_size: Total number of ranks

    Returns:
        meta_data: Dictionary of metadata loaded from checkpoint

    Side effects:
        - model parameters are updated in-place
        - optimizer states are updated in-place (if load_optimizer=True)

    Raises:
        ValueError: If checkpoint model_config doesn't match model's config
    """
    # Validate model config before loading
    _validate_model_config(checkpoint_dir, model, rank)

    # Use get_state_dict() to create proper template structure
    # This handles fresh optimizers correctly by constructing the full state structure
    opt_list = optimizers if (load_optimizer and optimizers is not None) else None
    model_state_dict, optim_state_dict = get_state_dict(model, opt_list)

    # Build state_dict for DCP load
    state_dict = {
        "model": model_state_dict,
        "optimizer": optim_state_dict if load_optimizer else None,
    }

    # DCP load (all ranks participate, each reads its own shard)
    dcp.load(
        state_dict=state_dict,
        storage_reader=FileSystemReader(checkpoint_dir),
    )

    # Use set_state_dict() to properly load into model and optimizer
    # This handles DTensor conversion and optimizer state restoration correctly
    set_state_dict(
        model,
        opt_list,
        model_state_dict=state_dict["model"],
        optim_state_dict=state_dict.get("optimizer") if load_optimizer else None,
    )

    # Load custom metadata
    meta_path = os.path.join(checkpoint_dir, "meta.json")
    with open(meta_path, "r") as f:
        meta_data = json.load(f)

    return meta_data


def load_metadata(checkpoint_dir: str) -> Dict[str, Any]:
    """
    Load only metadata from checkpoint without loading model/optimizer weights.

    Args:
        checkpoint_dir: Directory containing checkpoint

    Returns:
        meta_data: Dictionary of metadata from checkpoint
    """
    meta_path = os.path.join(checkpoint_dir, "meta.json")
    with open(meta_path, "r") as f:
        meta_data = json.load(f)
    return meta_data


def get_model_config(checkpoint_dir: str) -> Optional[Dict[str, Any]]:
    """
    Get model config from checkpoint metadata.

    Args:
        checkpoint_dir: Directory containing checkpoint

    Returns:
        The recorded model config dict (asdict of the GPTConfig the checkpoint
        was trained with) or None if not available (old checkpoint format)
    """
    meta = load_metadata(checkpoint_dir)
    return meta.get('model_config')


def config_from_meta(checkpoint_dir: str, config_cls, sequence_len: Optional[int] = None):
    """Blueprint recovery: rebuild the config a checkpoint was trained with.

    The inverse of the save-side recording (save_checkpoint_dcp writes
    asdict(model.config) into meta.json['model_config']). Keeping BOTH sides
    of this schema round-trip in core is what stops consumers from re-deriving
    architecture by convention. `config_cls` names the dataclass to rebuild
    into — callers get it from the trunk contract (trunk_cls.Config), so core
    stays ignorant of concrete architectures. `sequence_len` may be
    overridden — an instantiation choice (RoPE tables are computed, not
    stored), not a weight fact; everything else comes from the artifact.

    Returns None for old checkpoints that predate self-description.
    """
    saved = get_model_config(checkpoint_dir)
    if saved is None:
        return None
    # Tolerate schema drift both ways: unknown recorded keys are dropped,
    # fields newer than the recording fall back to config_cls defaults.
    known = {k: v for k, v in saved.items() if k in config_cls.__dataclass_fields__}
    if sequence_len is not None:
        known['sequence_len'] = sequence_len
    return config_cls(**known)


def gpt_config_from_meta(checkpoint_dir: str, sequence_len: Optional[int] = None):
    """config_from_meta specialized to GPTConfig (the name predates the
    trunk_cls seam; kept because it is honest — it does build a GPTConfig)."""
    from core.model.gpt import GPTConfig
    return config_from_meta(checkpoint_dir, GPTConfig, sequence_len=sequence_len)


def _validate_model_config(checkpoint_dir: str, model, rank: int = 0) -> None:
    """
    Validate that checkpoint model_config matches the model's config.

    Raises:
        ValueError: If configs don't match (with clear error message)
    """
    saved_config = get_model_config(checkpoint_dir)
    if saved_config is None:
        # Old checkpoint format, skip validation
        return

    if not hasattr(model, 'config'):
        # Model doesn't have config attribute, skip validation
        return

    cfg = model.config
    mismatches = []

    # Architecture identity — strictest of all: same config fields on a
    # different wiring is the ultimate "silently wrong model". Audited only
    # when both sides carry the fact (old checkpoints / bare models don't).
    saved_arch = load_metadata(checkpoint_dir).get('model_arch')
    model_arch = getattr(model, 'arch', None)
    if saved_arch is not None and model_arch is not None and saved_arch != model_arch:
        mismatches.append(f"model_arch: checkpoint={saved_arch}, model={model_arch}")

    # WEIGHT-SHAPING facts — these define what the stored tensors ARE. Some
    # mismatches would not even raise a shape error (n_head reshapes nothing,
    # yet changes attention semantics — a silently wrong model), so all are
    # strictly enforced.
    for field in ('n_layer', 'n_embd', 'n_head', 'n_kv_head', 'vocab_size', 'n_token_types'):
        if field in saved_config and saved_config[field] != getattr(cfg, field, None):
            mismatches.append(
                f"{field}: checkpoint={saved_config[field]}, model={getattr(cfg, field, None)}")

    if mismatches:
        raise ValueError(
            f"Model config mismatch with checkpoint {checkpoint_dir}:\n  " +
            "\n  ".join(mismatches)
        )

    # sequence_len is an INSTANTIATION choice, not a weight fact (RoPE tables
    # are computed, not stored) — running at a different context length than
    # trained is legitimate (e.g. short-context inference). Inform, don't block.
    if saved_config.get('sequence_len') != cfg.sequence_len and rank == 0:
        print(f"  note: sequence_len differs from checkpoint "
              f"(checkpoint={saved_config.get('sequence_len')}, model={cfg.sequence_len}) "
              f"— allowed (instantiation choice)")


def load_model_only(
    checkpoint_dir: str,
    model,
    rank: int = 0,
    world_size: int = 1,
) -> None:
    """
    Load only model weights from checkpoint (for pretrained initialization).

    Optimizer and dataloader states are NOT loaded - training starts fresh.
    Use this for initializing a new training run from pretrained weights.

    Args:
        checkpoint_dir: Directory containing checkpoint
        model: FSDP model (will be filled with loaded weights)
        rank: Current rank
        world_size: Total number of ranks

    Side effects:
        - model parameters are updated in-place
        - optimizer and dataloader states are ignored

    Raises:
        ValueError: If checkpoint model_config doesn't match model's config
    """
    # Validate model config before loading
    _validate_model_config(checkpoint_dir, model, rank)

    # Use get_state_dict() to create proper template structure for model only
    # Pass empty list instead of None (get_state_dict expects iterable)
    model_state_dict, _ = get_state_dict(model, optimizers=[])

    # Build state_dict for DCP load (model only)
    state_dict = {
        "model": model_state_dict,
    }

    # DCP load (all ranks participate, each reads its own shard)
    dcp.load(
        state_dict=state_dict,
        storage_reader=FileSystemReader(checkpoint_dir),
    )

    # Use set_state_dict() to properly load into model
    # Pass empty list instead of None (set_state_dict expects iterable)
    set_state_dict(
        model,
        optimizers=[],
        model_state_dict=state_dict["model"],
        optim_state_dict=None,
    )

    print(f"[Rank {rank}] Loaded model weights from {checkpoint_dir}")


def find_latest_checkpoint(base_dir: str) -> Optional[str]:
    """
    Find the latest checkpoint directory in base_dir.

    Args:
        base_dir: Base directory containing checkpoint directories

    Returns:
        Path to latest checkpoint directory, or None if not found

    Example:
        base_dir = "/path/to/checkpoints"
        latest = find_latest_checkpoint(base_dir)
        # Returns: "/path/to/checkpoints/step_002000"
    """
    import glob
    import re

    if not os.path.exists(base_dir):
        return None

    # Find all step_* directories
    pattern = os.path.join(base_dir, "step_*")
    checkpoint_dirs = glob.glob(pattern)

    if not checkpoint_dirs:
        return None

    # Extract step numbers and sort
    checkpoints = []
    for path in checkpoint_dirs:
        dirname = os.path.basename(path)
        match = re.fullmatch(r"step_(\d+)", dirname)
        if match:
            step = int(match.group(1))
            checkpoints.append((step, path))

    if not checkpoints:
        return None

    # Return the checkpoint with highest step number
    checkpoints.sort(key=lambda x: x[0], reverse=True)
    return checkpoints[0][1]


def resolve_checkpoint_path(
    resume_from: Optional[str],
    save_dir: Optional[str] = None,
    rank: int = 0
) -> Optional[str]:
    """
    Intelligently resolve checkpoint path with auto-detection support.

    This function provides smart checkpoint path resolution:
    1. If resume_from is None/empty -> return None (afresh run)
    2. If resume_from == "auto" -> check save_dir for latest checkpoint
       - If save_dir has checkpoints -> return latest
       - If save_dir doesn't exist or has no checkpoints -> return None (afresh run)
    3. If resume_from is an explicit path:
       - If path doesn't exist -> raise FileNotFoundError (fail-fast)
       - If path is a DCP checkpoint -> return it directly
       - If path is a directory with step_* subdirs -> auto-detect latest
       - Otherwise -> raise FileNotFoundError

    Args:
        resume_from: Path to checkpoint, directory, or "auto"
        save_dir: Directory where checkpoints are saved (required for "auto" mode)
        rank: Current rank (for printing messages on rank 0 only)

    Returns:
        Resolved checkpoint path, or None if no checkpoint found

    Raises:
        FileNotFoundError: If explicit path doesn't exist (fail-fast to prevent training mistakes)
        ValueError: If resume_from is "auto" but save_dir is not provided

    Examples:
        # Auto-resume mode (afresh if no checkpoint, resume if found)
        resolve_checkpoint_path("auto", save_dir="checkpoints/d12_run1")
        # Prints: "🔄 Auto-resume: Found checkpoint in checkpoints/d12_run1"
        # Returns: "checkpoints/d12_run1/step_002000" (or None if no checkpoint)

        # Explicit DCP checkpoint (fail-fast if not found)
        resolve_checkpoint_path("checkpoints/step_001000")
        # Returns: "checkpoints/step_001000" (or raises FileNotFoundError)

        # Explicit directory (auto-detect latest, fail-fast if not found)
        resolve_checkpoint_path("checkpoints/d12_run1")
        # Prints: "📁 Auto-detected latest checkpoint: checkpoints/d12_run1/step_002000"
        # Returns: "checkpoints/d12_run1/step_002000" (or raises FileNotFoundError)

        # No resume
        resolve_checkpoint_path(None)
        # Returns: None
    """
    # Case 1: No resume path provided (afresh run)
    if not resume_from:
        return None

    # Case 2: Auto-resume mode
    if resume_from.lower() == "auto":
        # Auto mode requires save_dir
        if not save_dir:
            raise ValueError(
                "resume_from='auto' requires save_dir to be specified in checkpoint config"
            )

        # Expand environment variables in save_dir
        save_dir = os.path.expandvars(save_dir)

        # Check if save_dir exists
        if not os.path.exists(save_dir):
            # save_dir doesn't exist yet (first run) - start fresh
            if rank == 0:
                print(f"🆕 Auto-resume: No checkpoint directory found")
                print(f"   Directory: {save_dir}")
                print(f"   Starting fresh training")
            return None

        # Try to find latest checkpoint in save_dir
        latest_checkpoint = find_latest_checkpoint(save_dir)
        if latest_checkpoint:
            # Found checkpoint - resume from it
            if rank == 0:
                step_name = os.path.basename(latest_checkpoint)
                print(f"🔄 Auto-resume: Found checkpoint in {save_dir}")
                print(f"   Resuming from: {step_name}")
            return latest_checkpoint
        else:
            # save_dir exists but no checkpoints yet - start fresh
            if rank == 0:
                print(f"🆕 Auto-resume: No checkpoints found in {save_dir}")
                print(f"   Starting fresh training")
            return None

    # Case 3: Explicit path (fail-fast if doesn't exist)
    # Expand environment variables
    resume_from = os.path.expandvars(resume_from)

    # Path must exist (fail-fast to catch typos)
    if not os.path.exists(resume_from):
        raise FileNotFoundError(
            f"Checkpoint path does not exist: {resume_from}\n"
            f"Hint: Use checkpoint.resume_from=auto for automatic resume (will start fresh if no checkpoint exists)"
        )

    # Case 3a: Not a directory (e.g., legacy checkpoint file)
    if not os.path.isdir(resume_from):
        return resume_from

    # Case 3b: Check if it's a DCP checkpoint directory
    # DCP checkpoints have meta.json and .metadata files
    meta_json = os.path.join(resume_from, "meta.json")
    if os.path.exists(meta_json):
        # It's a valid DCP checkpoint, return directly
        return resume_from

    # Case 3c: Check if directory contains step_* subdirectories
    latest_checkpoint = find_latest_checkpoint(resume_from)
    if latest_checkpoint:
        # Auto-detected latest checkpoint
        if rank == 0:
            step_name = os.path.basename(latest_checkpoint)
            print(f"📁 Auto-detected latest checkpoint: {latest_checkpoint}")
            print(f"   (from directory: {resume_from}, latest: {step_name})")
        return latest_checkpoint

    # Case 3d: Directory exists but no checkpoints found (fail-fast)
    raise FileNotFoundError(
        f"Directory exists but contains no valid checkpoints: {resume_from}\n"
        f"Hint: Use checkpoint.resume_from=auto for automatic resume (will start fresh if no checkpoint exists)"
    )


